package Operators;

public class Binary_operators {
    public static void main(String[] args) {
        //+//operator
        int a=12;
        int b=34;
        int sum=a+b;
        System.out.println(sum);



        //-operator//
        int c =34;
        int d=12;
        int diff=c-d;
        System.out.println(diff);

        //* mul operator//
        int e=12;
        int f=56;
        int mul=e*f;
        System.out.println(mul);

        // / operator//
        int g=4;
        int h=2;
        int div=g/h;
        System.out.println(div);

        // % operator//
        int i=4;
        int j=3;
        int modulo=i%j;
        System.out.println(modulo);

        }
}
