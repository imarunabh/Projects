package Operators;

public class unary_operator {
    public static void main(String[] args) {
    /////////////////pre increment ////////////////////////

        //  ++  //
        int  a=23;
        System.out.println(++a);

        // --//
        int b=34;
        System.out.println(--b);

    ///////////////post increment//////////////////////////

        // + //
        int c=34;
        c++;
        System.out.println(c);

        // - //
        int d=45;
        d++;
        System.out.println(d);


    }
}
